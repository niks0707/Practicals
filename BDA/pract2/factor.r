# Creating a factor
gender <- factor(c("Male", "Female", "Male", "Female", "Trans"))

# Printing the factor
print(gender)




# Factor in R and its Type:
# In R, a factor is a data type used to represent categorical or discrete data. It is used to store variables that have a limited number of distinct categories or levels. Factors are important for statistical analysis, especially when dealing with categorical variables. They are used to encode nominal or ordinal data.
